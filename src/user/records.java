package user;

import java.util.List;

public class records {

	private List playDate;
	private List playTeam;
	private List myTeam;
	private List url;
	
	public List getPlayDate() {
		return playDate;
	}
	public void setPlayDate(List playDate) {
		this.playDate = playDate;
	}
	public List getPlayTeam() {
		return playTeam;
	}
	public void setPlayTeam(List playTeam) {
		this.playTeam = playTeam;
	}
	public List getMyTeam() {
		return myTeam;
	}
	public void setMyTeam(List myTeam) {
		this.myTeam = myTeam;
	}
	public List getUrl() {
		return url;
	}
	public void setUrl(List url) {
		this.url = url;
	}
	
	
}
